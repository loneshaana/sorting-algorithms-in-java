    public static void main(String[] args) {
        int size =5;
        int c = 64;
        int inc =1;
        for(int i=1;i<=size;i++){
            for(int k=1;k<=size;k++) {
                for (int s = size; s > i; s--) {
                    System.out.print(" ");
                }

                System.out.print( (char)(c+inc));

                for (int z = 1; z <= i; z++) {
                    if (i != z)
                        System.out.print("  ");
                }

                System.out.print("B");
                for (int s = size; s > i; s--) {
                    System.out.print(" ");
                }
            }
            inc++;
            System.out.println();
        }
    }
